package com.javarush.modul1.lesson2;

public class Example11 {
    public static void main(String[] args) {
        int a = 10;
        int b = 3;

        System.out.println("a+b=" + (a + b));
        System.out.println("a-b=" + (a - b));
        System.out.println("a*b=" + (a * b));
        System.out.println("a/b=" + (a / b));
        System.out.println("a%b=" + (a % b));


        System.out.println("a+b=" + a + b); // a+b=10, a+b=103

    }
}
